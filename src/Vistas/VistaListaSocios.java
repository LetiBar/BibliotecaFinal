    private void jBModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBModificarActionPerformed

        int filaSeleccionada = jTable1.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecciona una fila para modificar.");
            return;
        }

        // Habilitamos la edici칩n en la celda de cada columna
        jTable1.editCellAt(filaSeleccionada, 1);
        jTable1.editCellAt(filaSeleccionada, 2);
        jTable1.editCellAt(filaSeleccionada, 3);
        jTable1.editCellAt(filaSeleccionada, 4);
        jTable1.editCellAt(filaSeleccionada, 5);
        jTable1.editCellAt(filaSeleccionada, 6);

        // Coloca el foco en la celda para permitir la edici칩n
        jTable1.getEditorComponent().requestFocus();

        // Obtenemos el ID del lector seleccionado en la tabla (asumiendo que est치 en la primera columna)
        int idLector = (int) jTable1.getValueAt(filaSeleccionada, 0);

        // Buscamos el lector en la base de datos utilizando su ID
        LectorData lecData = new LectorData();
        Lector lectorSeleccionado = lecData.buscarLectorPorId(idLector);

        // Obtenemos los datos editados de la fila seleccionada
        String nuevoApellido = (String) jTable1.getValueAt(filaSeleccionada, 1);
        String nuevoNombre = (String) jTable1.getValueAt(filaSeleccionada, 2);
        String nuevoDni = (String) jTable1.getValueAt(filaSeleccionada, 3);
        String nuevoDireccion = (String) jTable1.getValueAt(filaSeleccionada, 4);
        String nuevoTelefono = (String) jTable1.getValueAt(filaSeleccionada, 5);
        String nuevoMail = (String) jTable1.getValueAt(filaSeleccionada, 6);

        // Actualizamos los datos en el objeto Lector
        lectorSeleccionado.setApellido(nuevoApellido);
        lectorSeleccionado.setNombre(nuevoNombre);
        lectorSeleccionado.setDni(Integer.parseInt(nuevoDni));
        lectorSeleccionado.setDireccion(nuevoDireccion);
        lectorSeleccionado.setTelefono(Integer.parseInt(nuevoTelefono));
        lectorSeleccionado.setMail(nuevoMail);

        // Actualizamos el lector en la base de datos
        lecData.modificarLector(lectorSeleccionado);

        // Refrescamos la tabla para mostrar los cambios en la interfaz
        modelo.setValueAt(nuevoApellido, filaSeleccionada, 1);
        modelo.setValueAt(nuevoNombre, filaSeleccionada, 2);
        modelo.setValueAt(nuevoTelefono, filaSeleccionada,3);
        modelo.setValueAt(nuevoDireccion, filaSeleccionada, 4);
        modelo.setValueAt(nuevoMail, filaSeleccionada, 6);

    }//GEN-LAST:event_jBModificarActionPerformed

