    private void cargarLista(Lector lector) {
        modelo.addRow(new Object[]{lector.getIdLector(), lector.getApellido(), lector.getNombre(), lector.getDni(), lector.getDireccion(), lector.getTelefono(), lector.getMail(), lector.isActivo()});
    }

